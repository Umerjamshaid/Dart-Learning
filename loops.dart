void main() {
  //For loops
  var num = 6;
  for (var i = num; i >= 1; i--) {
    print(i);
  }

  //For in Loops
  var names = ["Jhon", "Umer", "Jamshiad", "Usman"];
  for (var name in names) {
    print(name);
  }

  //While Loops
  while (num >= 1) {
    print(num);
    num--;
  }
}
